let X = parseInt(prompt("Ingrese un número entero (X):"));

console.log("=== EJERCICIO 05 ===");
console.log("Número ingresado:", X);

if (X % 2 === 0) {
    console.log("El número", X, "es PAR.");
} else {
    console.log("El número", X, "es IMPAR.");
}
